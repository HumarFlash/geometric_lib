import math


def area(r):
    ''' Return area of the circle.
    
            Accepts:
                r (int): Radius of the circle

            Return:
                area (r): Area of the circle
    '''
    return math.pi * r * r


def perimeter(r):
    ''' Return length of the circle

            Accepts:
                r (int): Radius of the circle

            Return:
                perimiter (r): Perimiter of the circle
    '''
    return 2 * math.pi * r

