def area(a, b):
    ''' Return area of the rectangle

            Accepts:
                a (int): first side of the rectangle
                b (int): second side of the rectangle
            Return:
                area (int): area of the rectangle
    '''
    return a * b 

def perimeter(a, b):
    ''' Return perimeter of the rectangle

            Accepts:
                a (int): first side of the rectangle
                b (int): second side of the rectangle
            Return:
                perimeter (int): perimeter of the rectangle
    '''
    return 2*(a + b) 
