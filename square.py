
def area(a):
    ''' Return area of the square

            Accepts:
                a (int): side of the square
            Return:
                area(int): area of the square
    '''
    return a * a


def perimeter(a):
    ''' Return perimeter of the square

            Accepts:
                a (int): side of the square
            Return:
                perimeter (int): perimeter of the square
    '''
    return 4 * a
